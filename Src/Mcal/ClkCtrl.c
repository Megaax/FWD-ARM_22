/******************************************************************************
* \Syntax          : void IntCrtl_Init(void)                                      
* \Description     : initialize Nvic\SCB Module by parsing the Configuration 
*                    into Nvic\SCB registers                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/
void ClkCrtl_Init(void)
{
	
	#if RCC_PLL_MODE    ==	PLL_ON
	
		CLR_BIT(RCC,13);		 	/* DISABLE PLL Power Down*/
		CLR_BIT(RCC,11);     	/* BYPASS OUTPUT TO PLL */
	
	#else
	
		SET_BIT(RCC,13);		 /* PLL Power Down*/
		SET_BIT(RCC,11);     /* BYPASS OUTPUT TO OSC*/

	#endif
	
	#if  RCC_CLOCK_TYPE == PIOSC
			RCC|=(0x1<<4);
			RCC|=(SYS_DIVISOR<<23);  /*SET CLOCK DIVISOR*/
	
	#elif RCC_CLOCK_TYPE == MOSC
			RCC &=~(0x0<<4);
			CLR_BIT(RCC,0);
			RCC|=(SYS_DIVISOR<<23);  /*SET CLOCK DIVISOR*/
			RCC|=(XTAL_VALUE<<6);  /*SET CLOCK DIVISOR*/

	#elif RCC_CLOCK_TYPE == PIOSC_DIV_4
			RCC|=(0x2<<4);	
			RCC|=(SYS_DIVISOR<<23);  /*SET CLOCK DIVISOR*/
	
	#elif RCC_CLOCK_TYPE == LFIOSC
			RCC|=(0x3<<4);
			RCC|=(SYS_DIVISOR<<23);  /*SET CLOCK DIVISOR*/
			
	#endif
    


}



